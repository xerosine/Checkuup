import mongoose, { model, Schema } from "mongoose";

const contactSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    subject: {
        type: String,
        required: true
    },
    message: {
        type: String,
        required: true
    }
})

const Contact = model("Contact", contactSchema)


const newsletterSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    }
})

const Newsletter = model("Newsletter", newsletterSchema)

export {Contact, Newsletter}